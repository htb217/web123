           NEW YORK, Hoa Kỳ -  Hãng thời trang cao cấp của Pháp Christian Dior cho biết hôm thứ Hai rằng họ sẽ ấn định lịch trình diễn thời trang cho năm nay bắt đầu từ tháng 7 với màn trình diễn catwalk của Ý - nhưng không có khán giả hàng đầu nổi tiếng là những người hạng A.

Cuộc khủng hoảng coronavirus đã thúc đẩy các thương hiệu cao cấp phải suy nghĩ lại về cách trình bày các bộ sưu tập, với một số chọn không tham gia các sự kiện tốn kém và hạn chế số lượng quần áo mà họ sản xuất.

Dior, một trong những nhãn hiệu lớn nhất của tập đoàn LVMH, cho biết họ đang duy trì lịch các tuần lễ thời trang trong ngành đưa người mua và các blogger đến Paris, đồng thời sẽ sản xuất các bộ sưu tập khác ở giữa.

Giám đốc điều hành Pietro Beccari cho biết nó sẽ ra mắt một loạt "hành trình" giữa mùa tại thành phố Lecce của Ý vào ngày 22 tháng 7 với một chương trình trực tiếp được phát trực tiếp , sau khi một buổi thuyết trình được lên kế hoạch cho tháng 5 bị hoãn lại.

Beccari nói: “Chúng tôi muốn gửi một thông điệp về sự ủng hộ, về hy vọng, về sự lạc quan và sự tái sinh. "Tôi đang nghĩ về những nhà cung cấp lớn nhưng cũng có những nhà cung cấp nhỏ, nhiều doanh nghiệp gia đình nhỏ của các nghệ nhân ở Pháp và ở Ý ... nhiều người trong số họ không biết và vẫn không biết cách tồn tại."

Các thương hiệu cao cấp thường dựa vào các công ty để sản xuất vải sang trọng hoặc các sản phẩm yêu cầu xử lý đặc biệt, chẳng hạn như các mặt hàng được phủ lông vũ hoặc nhuộm bằng tay.

Theo Beccari, Dior đã lên kế hoạch tiến hành một buổi trình diễn thời trang nữ giới thường được tổ chức tại Paris vào tháng 9, đồng thời cho biết thêm rằng đến lúc đó nó thậm chí có thể thu hút khán giả ở hàng ghế đầu.

Nhà thiết kế Maria Grazia Chiuri của Dior cho biết: “Tuần lễ thời trang không chỉ quan trọng đối với gia đình thời trang, mà nó còn quan trọng đối với thành phố nơi diễn ra các buổi trình diễn .

Các sự kiện thời trang thường là hoạt động kinh doanh lớn đối với các thành phố như Paris, với các khách sạn thường tập trung vào thời kỳ đó.
